#include <stdio.h>

int main()
{
     int arr[100], n, i, j, count = 0;

    
    printf("Enter the number of elements in array: ");
    scanf("%d", &n);
    
    printf("Enter %d integers:\n", n);
    for(i = 0; i < n; i++) {
        scanf("%d", &arr[i]);
    }

    for(i = 0; i < n; i++) {
        for(j = i + 1; j < n; j++) {
            if(arr[i] == arr[j]) {
                count++;
                break;
            }
        }
    }

    printf("Total number of duplicate elements found: %d", count);

    return 0;
}
